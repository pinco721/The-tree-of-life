// Функция для получения персоны по ID
function getPersonById(id) {
    return familyData.persons.find(p => p.id === id);
}

// Функция для получения детей персоны
function getChildren(personId) {
    return familyData.persons.filter(p => p.parents && p.parents.includes(personId));
}

// Функция для получения супруга/супруги
function getSpouse(personId) {
    const person = getPersonById(personId);
    if (person && person.spouse) {
        return getPersonById(person.spouse);
    }
    // Проверяем обратную связь
    return familyData.persons.find(p => p.spouse === personId);
}
